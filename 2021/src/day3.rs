//The submarine has been making some odd creaking noises, so you ask it to produce a diagnostic report just in case.

//The diagnostic report (your puzzle input) consists of a list of binary numbers which, when decoded properly, can tell you many useful things about the conditions of the submarine. The first parameter to check is the power consumption.

//You need to use the binary numbers in the diagnostic report to generate two new binary numbers (called the gamma rate and the epsilon rate). The power consumption can then be found by multiplying the gamma rate by the epsilon rate.

//Each bit in the gamma rate can be determined by finding the most common bit in the corresponding position of all numbers in the diagnostic report. For example, given the following diagnostic report:

//00100
//11110
//10110
//10111
//10101
//01111
//00111
//11100
//10000
//11001
//00010
//01010

//Considering only the first bit of each number, there are five 0 bits and seven 1 bits. Since the most common bit is 1, the first bit of the gamma rate is 1.

//The most common second bit of the numbers in the diagnostic report is 0, so the second bit of the gamma rate is 0.

//The most common value of the third, fourth, and fifth bits are 1, 1, and 0, respectively, and so the final three bits of the gamma rate are 110.

//So, the gamma rate is the binary number 10110, or 22 in decimal.

//The epsilon rate is calculated in a similar way; rather than use the most common bit, the least common bit from each position is used. So, the epsilon rate is 01001, or 9 in decimal. Multiplying the gamma rate (22) by the epsilon rate (9) produces the power consumption, 198.

//Use the binary numbers in your diagnostic report to calculate the gamma rate and epsilon rate, then multiply them together. What is the power consumption of the submarine? (Be sure to represent your answer in decimal, not binary.)
pub fn calculate_power_consumption() {
    let num_digits = 12;
    let final_diagnostic: u16 = (1..=num_digits)
        .fold(0, |acc, n| {
            let total_bits = DIAGNOSTIC.iter().fold(0, |total_bits, binary_num| {
                // since we are representing these 12 bit numbers as u16 we need to rotate left by
                // 16-12
                let binary_num = binary_num.rotate_left(16 - num_digits);
                total_bits + (binary_num.rotate_left(n) & 1)
            });

            if usize::from(total_bits) >= DIAGNOSTIC.len() / 2 {
                //if we have more on bits, set that bit to 1
                let bit = 0b1000_0000_0000_0000u16.rotate_right(n as u32);
                return acc ^ bit;
            }
            acc
        })
        .rotate_right(16 - num_digits - 1);
    let gamma = final_diagnostic;
    let epsilon = !final_diagnostic ^ 0b1111_0000_0000_0000;
    println!("{}", gamma as u64 * epsilon as u64)
}

